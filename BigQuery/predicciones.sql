SELECT
  CUANTIA,
  FECHA_SENTENCIA,
  DATE_ADD(FECHA_SENTENCIA, INTERVAL 86 DAY) AS FECHA_COBRO_ESPERADA,
  ROUND(LEAST(50000.0, GREATEST(1.0, predicted_COSTAS)), 2) AS COSTAS_PREDICHAS
FROM ML.PREDICT(
  MODEL `Modelo_costas.modelo_costas_lr`,
  (
    SELECT 1500.00 AS CUANTIA, DATE '2025-01-10' AS FECHA_SENTENCIA UNION ALL
    SELECT 7800.00, DATE '2025-02-05' UNION ALL
    SELECT 25000.00, DATE '2025-03-20'
  )
);
